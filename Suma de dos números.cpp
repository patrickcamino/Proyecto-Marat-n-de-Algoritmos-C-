//Suma de dos números  //
#include <iostream>
using namespace std;

int main() {
    // Declaración de variables
    float num1, num2, suma;

    // Entrada de datos
    cout << "Ingrese el primer número: ";
    cin >> num1;

    cout << "Ingrese el segundo número: ";
    cin >> num2;

    // Proceso
    suma = num1 + num2;

    // Salida
    cout << "La suma de los dos números es: " << suma << endl;

    return 0;
}
